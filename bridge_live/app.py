# Helpers
# ---------------------------------------------------------------------------


async def _resolve_yaml_payload(yaml_text: str, upload: UploadFile | None) -> str:
    """Pick whichever side of the form submitted content; prefer paste."""
    if yaml_text and yaml_text.strip():
        return yaml_text
    if upload is not None:
        raw = await upload.read()
        try:
            return raw.decode("utf-8")
        except UnicodeDecodeError:
            return ""
    return ""


def _scaffold_for_topic(topic: str) -> str:
    """Render the canned ``?topic=`` scaffold for a launch deep link.

    The slug is sanitised because schema enforces ``[a-z0-9-]{3,64}``
    on ``name``. Falling back to ``topic`` keeps the scaffold simple
    while still validating cleanly when the user submits it.
    """
    slug = "".join(ch if ch.isalnum() else "-" for ch in topic.lower()).strip("-")[:48]
    if len(slug) < 3:
        slug = "topic"
    return _TOPIC_SCAFFOLD.format(topic=topic.strip() or "this topic", slug=slug)


def _seed_showcase() -> None:
    """Populate the passport store with the 8 bundled bridge templates.

    Each entry in ``grok_build_bridge/templates/INDEX.yaml`` becomes a
    seeded passport — clicking the "Showcase" tab in production should
    show real, runnable agents on first paint, not an empty state.
    """
    try:
        templates_root = resources.files("grok_build_bridge.templates")
        index_path = templates_root / "INDEX.yaml"
        if not index_path.is_file():
            return
        index = yaml.safe_load(index_path.read_text(encoding="utf-8")) or {}
    except (OSError, yaml.YAMLError):  # pragma: no cover — packaging bug
        return
    entries = index.get("templates") or []

    for entry in entries:
        if not isinstance(entry, dict):
            continue
        files = entry.get("files") or []
        yaml_resource: Any | None = None
        local_code_resource: Any | None = None
        for spec in files:
            if not isinstance(spec, dict):
                continue
            src = spec.get("src")
            if not isinstance(src, str):
                continue
            res = templates_root / src
            if src.endswith(".yaml") and yaml_resource is None:
                yaml_resource = res
            elif src.endswith(".py") and local_code_resource is None:
                local_code_resource = res
        if yaml_resource is None or not yaml_resource.is_file():
            continue
        yaml_text = yaml_resource.read_text(encoding="utf-8")
        extra_code: str | None = None
        if local_code_resource is not None and local_code_resource.is_file():
            extra_code = local_code_resource.read_text(encoding="utf-8")
        try:
            passport = passport_from_yaml(yaml_text, extra_code=extra_code, seeded=True)
        except BridgeConfigError:  # pragma: no cover — bundled templates always validate
            continue
        store.save(passport)


# Module-level instance so ``uvicorn bridge_live.app:app`` works without
# the user having to write ``--factory``.
app = create_app()


# Re-export for callers that prefer ``urlencode`` from the same place.
__all__ = ["app", "create_app", "urlencode"]
